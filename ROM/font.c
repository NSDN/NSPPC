const uint8_t* fontGet(uint8_t id) {
    switch (id) {
        case 0x00: return font_6x8;
        case 0x01: return font_9x8;
        case 0xFF: return yakumo_pc_logo;
        default: return font_6x8;
    }
}

void fontSize(const uint8_t* font, uint8_t* w, uint8_t* h) {
    if (font == font_6x8) {
        *w = 6; *h = 8;
    } else if (font == font_9x8) {
        *w = 9; *h = 8;
    } else if (font == yakumo_pc_logo) {
        *w = 48; *h = 59;
    }
}
